let stringObject = new String('Hello, World!');


// charAt methods (Returns the character at a specified index)

console.log(stringObject.charAt(0));
console.log(stringObject.charAt(5));

// charCodeAt (Returns the Unicode of the character at a specified index.)

console.log(stringObject.charCodeAt(1));
console.log(stringObject.charCodeAt(0));

// contac (join two or more string)

console.log(stringObject.concat("How are U?"));


// all character are Uowercase

console.log(stringObject.toLowerCase());

// all character are Upperrcase
console.log(stringObject.toUpperCase());

// trim methods remove white space from both the side
const txt = "  I love js  ";
console.log(txt.trim());








