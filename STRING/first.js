// Single Quotes: We can create a string using single quotes (').
let singleQuoteString = 'Hello World!';


// Double Quotes: we can create a string using single quotes (').
let doubleQuoteString = "Hello World!";


// Backticks (Template Literals): Introduced in ES6, backticks (`) allow for multi-line strings and string interpolation.

let templateLiteralString = `Hello, 
World!`;
console.log(templateLiteralString);

let name = 'John';
console.log(name);

let greeting = `Hello, ${name}!`;
console.log(greeting);

// String Constructor: we can create a string using the String constructor
let stringObject = new String('Hello, World!');
